function [rtnR, rtnI] = fft2D(xR, xI, N)
% rtn -> return value
  [rtnR] = zeros(N, N);
  [rtnI] = zeros(N, N);
  mc_t98 = 1;
  for i = (mc_t98 : N);
    [mc_t91] = xR(i, :);
    [mc_t92] = xI(i, :);
    mc_t93 = N;
    [resR, resI] = fftSimple(mc_t91, mc_t92, mc_t93);
    mc_t97 = 1;
    [k] = colon(mc_t97, N);
    [mc_t87] = resR(k);
    rtnR(i, k) = mc_t87;
    [mc_t88] = resI(k);
    rtnI(i, k) = mc_t88;
  end
  [rtnR] = transpose(rtnR);
  [rtnI] = transpose(rtnI);
  mc_t100 = 1;
  for i = (mc_t100 : N);
    [mc_t94] = rtnR(i, :);
    [mc_t95] = rtnI(i, :);
    mc_t96 = N;
    [resR, resI] = fftSimple(mc_t94, mc_t95, mc_t96);
    mc_t99 = 1;
    [k] = colon(mc_t99, N);
    [mc_t89] = resR(k);
    rtnR(i, k) = mc_t89;
    [mc_t90] = resI(k);
    rtnI(i, k) = mc_t90;
  end
  [rtnR] = transpose(rtnR);
  [rtnI] = transpose(rtnI);
end
function [rtnR, rtnI] = fftSimple(xR, xI, N)
  mc_t150 = 1;
  [rtnR] = zeros(mc_t150, N);
  mc_t151 = 1;
  [rtnI] = zeros(mc_t151, N);
  mc_t173 = 1;
  [mc_t172] = eq(N, mc_t173);
  if mc_t172
    mc_t152 = 1;
    [mc_t101] = xR(mc_t152);
    mc_t153 = 1;
    rtnR(mc_t153) = mc_t101;
    mc_t154 = 1;
    [mc_t102] = xI(mc_t154);
    mc_t155 = 1;
    rtnI(mc_t155) = mc_t102;
  else 
    mc_t156 = 2;
    [mc_t113] = rdivide(N, mc_t156);
    [half] = floor(mc_t113);
    mc_t157 = 1;
    [eR] = zeros(mc_t157, half);
    mc_t158 = 1;
    [eI] = zeros(mc_t158, half);
    mc_t159 = 1;
    [dR] = zeros(mc_t159, half);
    mc_t160 = 1;
    [dI] = zeros(mc_t160, half);
    mc_t167 = 1;
    for k = (mc_t167 : half);
      mc_t161 = 2;
      [mc_t115] = times(mc_t161, k);
      mc_t162 = 1;
      [mc_t114] = minus(mc_t115, mc_t162);
      [mc_t103] = xR(mc_t114);
      eR(k) = mc_t103;
      mc_t163 = 2;
      [mc_t117] = times(mc_t163, k);
      mc_t164 = 1;
      [mc_t116] = minus(mc_t117, mc_t164);
      [mc_t104] = xI(mc_t116);
      eI(k) = mc_t104;
      mc_t165 = 2;
      [mc_t118] = times(mc_t165, k);
      [mc_t105] = xR(mc_t118);
      dR(k) = mc_t105;
      mc_t166 = 2;
      [mc_t119] = times(mc_t166, k);
      [mc_t106] = xI(mc_t119);
      dI(k) = mc_t106;
    end
    [VecER, VecEI] = fftSimple(eR, eI, half);
    [VecDR, VecDI] = fftSimple(dR, dI, half);
    mc_t170 = 1;
    for k = (mc_t170 : half);
      r = 1;
      mc_t168 = 2.0;
      [mc_t124] = uminus(mc_t168);
      [mc_t125] = pi();
      [mc_t122] = times(mc_t124, mc_t125);
      mc_t169 = 1;
      [mc_t123] = minus(k, mc_t169);
      [mc_t120] = times(mc_t122, mc_t123);
      mc_t121 = N;
      [rad] = rdivide(mc_t120, mc_t121);
      mc_t126 = r;
      [mc_t127] = cos(rad);
      [cR] = times(mc_t126, mc_t127);
      mc_t128 = r;
      [mc_t129] = sin(rad);
      [cI] = times(mc_t128, mc_t129);
      [mc_t134] = VecDR(k);
      mc_t135 = cR;
      [mc_t130] = times(mc_t134, mc_t135);
      [mc_t132] = VecDI(k);
      mc_t133 = cI;
      [mc_t131] = times(mc_t132, mc_t133);
      [tmpR] = minus(mc_t130, mc_t131);
      [mc_t140] = VecDR(k);
      mc_t141 = cI;
      [mc_t136] = times(mc_t140, mc_t141);
      [mc_t138] = VecDI(k);
      mc_t139 = cR;
      [mc_t137] = times(mc_t138, mc_t139);
      [tmpI] = plus(mc_t136, mc_t137);
      VecDR(k) = tmpR;
      VecDI(k) = tmpI;
    end
    mc_t171 = 1;
    for k = (mc_t171 : half);
      [mc_t142] = VecER(k);
      [mc_t143] = VecDR(k);
      [mc_t107] = plus(mc_t142, mc_t143);
      rtnR(k) = mc_t107;
      [mc_t144] = VecEI(k);
      [mc_t145] = VecDI(k);
      [mc_t108] = plus(mc_t144, mc_t145);
      rtnI(k) = mc_t108;
      [mc_t146] = VecER(k);
      [mc_t147] = VecDR(k);
      [mc_t109] = minus(mc_t146, mc_t147);
      [mc_t111] = plus(k, half);
      rtnR(mc_t111) = mc_t109;
      [mc_t148] = VecEI(k);
      [mc_t149] = VecDI(k);
      [mc_t110] = minus(mc_t148, mc_t149);
      [mc_t112] = plus(k, half);
      rtnI(mc_t112) = mc_t110;
    end
  end
end
